double elementmass(char *string)
{
	init_elementlist();
	double mass;
	double massMo = 0;
	double maxAbundance = 0;
	for(int i=0; i < elements[string].isotopecount; i++){
		if(maxAbundance < elements[string].abundance[i]){
			massMo = elements[string].isotope[i];
			maxAbundance = elements[string].abundance[i];
		}
	}
	mass = massMo;
	return mass;
}


double rdbe(vector<char*> compositions, vector<int> count)
{
	init_elementlist();
	double rdbevalue = 0;
	for (unsigned int i = 0; i < compositions.size(); i++)
	{
		int valence = elements[compositions[i]].valence;
		if (valence)
		{
			rdbevalue += (valence - 2) * count[i];
		}
	}
	rdbevalue = rdbevalue / 2;
	rdbevalue = rdbevalue + 1;
	return rdbevalue;
}

double formulaMass(vector<char*> compositions, vector<int> count)
{
	double formulamass = 0;
	init_elementlist();
	for (unsigned int i = 0; i < compositions.size(); i++)
	{
		formulamass += elementmass(compositions[i]) * (count[i]);
	}
	return formulamass;
}